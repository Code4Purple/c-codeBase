#include <iostream>
using namespace std;


int main() {
    // Naming the variable
    int x;
    // Assigning the value to the variable
    x = 5;
    // Printing the value of the variable
    cout << x << endl;


    // Declaring Multiple Variables
    int alpha, beta, car;
    // Initializing the variables
    alpha = 1;
    beta = 2;
    car = 3;
    // Printing the values of the variables
    cout << alpha << " " << beta << " " << car << endl;


    return 0;
}