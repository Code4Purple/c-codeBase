#include <iostream>

using namespace std;


int main(){

    // Original Values
    int startValue, endValue;
    startValue = 5;
    endValue = -1;
    cout << "Orginal End Value : " << endValue << endl;

    // Changing the value by the Start Value
    endValue = startValue;
    cout << "Changed End Value : " << endValue << endl;
    
}