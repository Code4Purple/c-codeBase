#include <iostream>

using namespace std;

int main() {
    // Addition
    cout << "Addition: " << 5 + 3 << endl;
    // Subtraction
    cout << "Subtraction: " << 5 - 3 << endl;
    // Multiplication
    cout << "Multiplication: " << 5 * 3 << endl;
    // Division
    cout << "Division: " << 15 / 3 << endl;

    // Oder of operations
    cout << "Order of operations: " << 5 + 3 * 2 + (5 * 5) << endl;
    
    
    return 0;
}